// Access Modifiers

// Ex. Home = Package, Room = Class

// 1. public: Accessible from anywhere (any class, package or subclass).  
// Ex. Public washrooms are accessible for everyone.

// 2. protected: Accessible within the same package or by subclasses in different packages. 
// Ex. Protected rooms are accessible only to the family members. means for parents as well as childern.   Ex. Food

// 3. private: Accessible only within the same class.
// Ex. Private rooms are accessible only to the person who has the key. for parents only      Ex. Bank locker

// 4. default: Accessible within the same package.
// Ex. Default rooms are accessible only to the family members.